temp = float(input("Enter temperature in degree Celcius: "))
Fahrenheit = temp * 9 / 5 + 32
Kelvin = temp + 273.15
print(
    f"""Temperature in degrees Fahrenheit: {Fahrenheit: .2f}
Temperature in degrees Kelvin: {Kelvin: .2f}"""
)
